# arithmetic operators,assignment operator,logocal operator,comparison operator

num = 45
num2 = 65
print(f"the sum of {num} and {num} is {num+num2}")
print(f"the difference of {num} and {num} is {num-num2}")
print(f"the division of {num} and {num} is {num/num2}")
print(f"the multiplication of {num} and {num} is {num*num2}")
print(f"the remainder of {num} and {num} is {num%num2}")
print(f"the exponent of {num} and {num} is {num**num2}")
print(f"the floor of {num} and {num} is {num//num2}")

num3 = 56
num4 = 90

#comparison operators

print(f"{num3} and {num4} are equals {num3==num4}")
print(f"{num3} and {num4} are not equals {num3!=num4}")
print(f"{num3} is greater than {num4} {num3>num4}")
print(f"{num3} is less than {num4} {num3<num4}")
print(f"{num3} is greater than or equals to  {num4} {num3>=num4}")
print(f"{num3} is less than or equals to  {num4} {num3<=num4}")

